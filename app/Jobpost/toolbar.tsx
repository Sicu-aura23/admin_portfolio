import React from "react";
import { Quill } from "react-quill";
import 'react-quill/dist/quill.snow.css';
import './quill-toolbar.css'; // Import the custom CSS file

// Undo and redo functions for Custom Toolbar
function undoChange(this: {
    quill: any; undo: () => void; redo: () => void; 
}) {
  this.quill.history.undo();
}
function redoChange(this: {
    quill: any; undo: (this: { undo: () => void; redo: () => void; }) => void; redo: () => void; 
}) {
  this.quill.history.redo();
}

// Add sizes to whitelist and register them
const Size = Quill.import("formats/size");
Size.whitelist = ["extra-small", "small", "medium", "large"];
Quill.register(Size, true);

// Add fonts to whitelist and register them
const Font = Quill.import("formats/font");
Font.whitelist = [
  "arial",
  "comic-sans",
  "courier-new",
  "georgia",
  "helvetica",
  "lucida"
];
Quill.register(Font, true);

// Add upper-alpha to list formats
const List = Quill.import("formats/list");
List.whitelist.push("upper-alpha");
Quill.register(List, true);

// Modules object for setting up the Quill editor
export const modules = {
  toolbar: {
    container: "#toolbar",
    handlers: {
      undo: undoChange,
      redo: redoChange
    }
  },
  history: {
    delay: 500,
    maxStack: 100,
    userOnly: true
  }
};

